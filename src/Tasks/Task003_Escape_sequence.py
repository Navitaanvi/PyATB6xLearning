"""
Escape sequence is a special combination of characters that represents something difficult to type directly in a string
It starts with a backslash(\)
"""

print("Hello\nWorld") #Hello
                    # World

print("Hello\tWorld") # Hello	World

print("C:\\users\\Navita") #o/p-->C:\users\Navita (use \\ to print \ )

print("She said, \"Python is fun!\"") #o/p-->She said, "Python is fun!  (use \" for printing double quotes)

print("I said,\"I love Python\"")

print("Anvi said, It\'s a rainy season")

print("Hello\bWorld") #HellWorld

print("Hello\rWorld") #o/p--World (\r moves the cursor back to the start of the line.)

print("Hello\fWorld")

print("\123") #S

print("\x56") #V
